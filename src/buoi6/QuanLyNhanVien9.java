    private void treeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_treeMouseClicked
        // TODO add your handling code here:
        tree.cancelEditing();
        TreePath path = tree.getSelectionPath();
        if(path == null) return;
        DefaultMutableTreeNode selectedNode = null;
        selectedNode = (DefaultMutableTreeNode)(path.getLastPathComponent());
        Object selecteObject = selectedNode.getUserObject();
        if (selectedNode == root) {
            this.curDepNode = this.curDepNode = null;
        }else
        {
            if (selecteObject instanceof Department) {
                this.curDepNode = selectedNode;
                this.curEmpNode=null;
            }else if(selecteObject instanceof Employee)
            {
                this.curEmpNode = selectedNode;
                this.curDepNode =  (DefaultMutableTreeNode)(selectedNode.getParent());
            }
        }
        viewDepaAndEmp();
    }//GEN-LAST:event_treeMouseClicked

