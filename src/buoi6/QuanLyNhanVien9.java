    private void loadData() {
        String s="";
        StringTokenizer stk;
        try {
            FileReader f = new FileReader(fileName);
            BufferedReader bf = new BufferedReader(f);
            while ((s = bf.readLine()) != null) {
                s = s.trim();
                boolean isDept = (s.charAt(s.length() - 1) == ':');
                stk = new StringTokenizer(s,"-:,");
                String code = stk.nextToken().trim();
                String name = stk.nextToken().trim();
                if(isDept){
                    curDepNode = new DefaultMutableTreeNode(new Department(code,name));
                    root.add(curDepNode);
                }
                else
                {
                    int salary = Integer.parseInt(stk.nextToken().trim());
                    curEmpNode = new DefaultMutableTreeNode(new Employee(code,name,salary));
                    curDepNode.add(curEmpNode);
                }  
            }
            bf.close(); f.close();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "Không đọc được file");
        }
    }
    private void viewDepaAndEmp()
    {
        Department curDep = null;
        Employee curEmp = null;
        if (curDepNode != null) {
            curDep = (Department) (curDepNode.getUserObject());
        }
        if (curEmpNode != null)
        {
            curEmp =(Employee) (curEmpNode.getUserObject());
        }
        this.txtCode.setText(curDep != null ? curDep.getDepCode(): "");
        this.txtName.setText(curDep != null ? curDep.getDepName(): "");
        this.txtCodeE.setText(curEmp != null ? curEmp.getEmpCode(): "");
        this.txtNameE.setText(curEmp != null ? curEmp.getEmpName(): "");
        this.txtSalary.setText(curEmp != null ? "" + curEmp.getSalary(): "");
        this.txtCode.setEditable(false);
        this.txtCodeE.setEditable(false);
    }
